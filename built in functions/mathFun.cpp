#include<iostream>
#include<cmath>
using namespace std;
int main(){
    cout<<"square root of 10 is :";
    cout<<sqrt(10)<<endl;
    cout<<"power of 10 is:";
    cout<<pow(10,2)<<endl;
    cout<<"absolute of 10.0 is :";
    cout<<abs(10.0)<<endl;
    cout<<"ceil of 0.5 is :";
    cout<<ceil(0.5)<<endl;
    cout<<"floor of 1.5 is:";
    cout<<floor(1.5)<<endl;
    cout<<"round of 10.9 is:";
    cout<<round(10.9)<<endl;
    cout<<"exponential of 4 is:";
    cout<<exp(4)<<endl;
    cout<<"sin of 10 is:";
    cout<<sin(10)<<endl;
    cout<<"tan of 12 is:";
    cout<<tan(12)<<endl;
    cout<<"log of 12 is:";
    cout<<log(12)<<endl;
    return 0;
}